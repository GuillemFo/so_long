#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	s1l;
	char	*str;
	int		i;

	i = 0;
	s1l = ft_strlen(s1);
	str = malloc (s1l + 1 * sizeof(char));
	if (!str)
		return (0);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
